  <h1>Community Guidelines</h1>
  <p>RuinMyTrip is a trustworthy travel community. Keep it useful, honest, and safe.</p>
  <h2>Do</h2>
  <ul>
    <li>Share real trips, honest reviews, and helpful guides.</li>
    <li>Be respectful. Travelers come from everywhere.</li>
    <li>Keep meetups public, friendly, and safe.</li>
    <li>Report content that breaks these rules.</li>
  </ul>
  <h2>Don't</h2>
  <ul>
    <li>No harassment, hate speech, threats, or discrimination.</li>
    <li>No sexual, adult, or hookup content. This is not a dating platform.</li>
    <li>No scams, spam, fake reviews, or paid manipulation.</li>
    <li>No sharing of anyone's precise or real-time location.</li>
    <li>No illegal or unsafe activity, and nothing involving minors.</li>
  </ul>
