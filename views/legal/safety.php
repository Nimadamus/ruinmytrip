  <h1>Meetup Safety</h1>
  <div class="callout"><b>Meetups are optional, public, community connections — never dating or hookups.</b> RuinMyTrip is built to help travelers meet safely, in the open.</div>
  <h2>How location works here</h2>
  <p>Meetups are tied to a <b>destination only</b>. We never store or share precise coordinates, home addresses, or real-time location. Exact meeting spots are public places, shared with confirmed attendees inside the app — not posted publicly.</p>
  <h2>Before you go</h2>
  <ul>
    <li>Meet in a busy, public place in daylight when you can.</li>
    <li>Tell a friend or family member where you're going and when.</li>
    <li>Check the host's and attendees' profiles and credibility.</li>
    <li>Arrange your own transport; keep your phone charged.</li>
  </ul>
  <h2>During the meetup</h2>
  <ul>
    <li>Trust your instincts. If something feels off, leave.</li>
    <li>Keep personal and financial details private.</li>
    <li>You're never obligated to stay.</li>
  </ul>
  <h2>Tools we give you</h2>
  <ul>
    <li><b>Age requirements:</b> 16+ to use RuinMyTrip, 18+ to host or attend meetups.</li>
    <li><b>Report & block:</b> one tap to report content or block a user.</li>
    <li><b>Moderation:</b> a human team reviews reports and removes bad actors.</li>
    <li><b>Privacy controls:</b> you decide who sees your travel plans.</li>
  </ul>
  <p>If you're ever in immediate danger, contact local emergency services first.</p>
